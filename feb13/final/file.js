  function validateURL() {
    var url = document.getElementById("url").value;
    var pattern = /^(https?:\/\/)?([\da-z\.-]+)\.([a-z\.]{2,6})([\/\w \.-]*)*\/?$/;
    var validDomains = [".com", ".in", ".co.in", ".org", ".net"];
    var isValid = false;
    for (var i = 0; i < validDomains.length; i++) {
      if (url.endsWith(validDomains[i])) {
        isValid = true;
        break;
      }
    }
    if (pattern.test(url) && isValid) {
        return true;
    } else {
        alert(url + " is NOT a valid  URL. Please enter valid URL");
        document.getElementById("url").value = "";
                return  false;
    }
  }

